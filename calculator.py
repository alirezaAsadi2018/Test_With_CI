class Calculator:
    def add(self, op1, op2):
        return op1 + op2

    def subtract(self, op1, op2):
        return op1 - op2

    def multiply(self, op1, op2):
        return op1 * op2

    def divide(self, op1, op2):
        return op1 // op2

    def average(self, op1, op2):
        return (op1 + op2) // 2
